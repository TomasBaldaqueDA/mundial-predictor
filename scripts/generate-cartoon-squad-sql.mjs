import fs from "node:fs"
import path from "node:path"

const REPO_ROOT = path.resolve(import.meta.dirname, "..")
const SOURCE_DIR = path.join(REPO_ROOT, "imgs_cartoons")
const OUT = path.join(REPO_ROOT, "supabase", "sql", "047_sync_cartoon_squads.sql")

const POSITION_SLOTS = [
  ...Array(3).fill("gk"),
  ...Array(8).fill("df"),
  ...Array(8).fill("md"),
  ...Array(7).fill("st"),
]

function sqlEscape(s) {
  return s.replace(/'/g, "''")
}

function photoUrlFor(team, fileName) {
  const ext = path.extname(fileName).toLowerCase()
  const base = fileName.slice(0, -path.extname(fileName).length)
  return `/imgs_cartoons/${encodeURIComponent(team)}/${encodeURIComponent(base)}${ext}`
}

function playersForTeam(team) {
  const dir = path.join(SOURCE_DIR, team)
  const files = fs
    .readdirSync(dir)
    .filter((f) => /\.(jpe?g|png|webp)$/i.test(f))
    .sort((a, b) => a.localeCompare(b, undefined, { sensitivity: "base" }))

  const fromImages = files.map((file, i) => ({
    name: file.replace(/\.(jpe?g|png|webp)$/i, ""),
    team,
    position: POSITION_SLOTS[i] ?? "md",
    photo_url: photoUrlFor(team, file),
  }))

  const out = [...fromImages]
  let slot = fromImages.length
  while (out.length < 26) {
    const position = POSITION_SLOTS[slot]
    const n = out.filter((p) => p.position === position).length + 1
    out.push({
      name: `${team} ${position.toUpperCase()} ${n}`,
      team,
      position,
      photo_url: null,
    })
    slot += 1
  }
  return out
}

function main() {
  const teams = fs
    .readdirSync(SOURCE_DIR, { withFileTypes: true })
    .filter((d) => d.isDirectory())
    .map((d) => d.name)
    .sort((a, b) => a.localeCompare(b))

  const lines = [
    "-- Auto-generated by: node scripts/generate-cartoon-squad-sql.mjs",
    "-- Syncs nations that have imgs_cartoons/{team}/*.jpg into five_a_side_players (26 per team).",
    "-- Requires column photo_url (045). Run refresh after apply.",
    "",
  ]

  for (const team of teams) {
    const rows = playersForTeam(team)
    lines.push(`DELETE FROM public.five_a_side_players WHERE team = '${sqlEscape(team)}';`)
    for (const r of rows) {
      const photo = r.photo_url ? `'${sqlEscape(r.photo_url)}'` : "NULL"
      lines.push(
        `INSERT INTO public.five_a_side_players (name, team, position, photo_url) VALUES ('${sqlEscape(r.name)}', '${sqlEscape(r.team)}', '${r.position}', ${photo});`
      )
    }
    lines.push("")
  }

  lines.push("SELECT public.refresh_five_a_side_player_stats();")
  lines.push("")

  fs.writeFileSync(OUT, lines.join("\n"), "utf8")
  console.log(`Wrote ${OUT} (${teams.length} teams)`)
}

main()
